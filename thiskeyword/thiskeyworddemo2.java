package thiskeyword;

public class thiskeyworddemo2 {
	
	int a,b; // instance variable
	
	void getValues (int a, int b) // method variable
	
	{
		this.a =a;
		this.b =b;
		
	}
	
	void display()
	
	{
		System.out.println(a);
		System.out.println(b);
	}
	

	public static void main(String[] args) {
		
		thiskeyworddemo2 th = new thiskeyworddemo2 ();	
		th.getValues(10, 20);
		th.display();
		
		
		//Keyword 'THIS' is a reference variable in Java that refers the object to current variable 
		
		//Keyword 'THIS' is a reference variable in Java that refers the object to current variable
		
		//Keyword 'THIS' is a reference variable in Java that refers the object to current variable
		
		//Keyword 'THIS' is a reference variable in Java that refers the object to current variable
		
		//Keyword 'THIS' is a reference variable in Java that refers the object to current variable
		
	}

}
